dictionary = ['all', 'an', 'and', 'as', 'closely', 'correct', 'equivocal',
              'examine', 'indication', 'is', 'means', 'minutely', 'or', 'scrutinize',
              'sign', 'the', 'to', 'uncertain']
sentence = input().split()
errors = [word for word in sentence if word not in dictionary]
if errors:
    print("\n".join(errors))
else:
    print("OK")